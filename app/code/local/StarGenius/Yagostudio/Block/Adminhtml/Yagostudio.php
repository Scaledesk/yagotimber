class StarGenius_Yagostudio_Block_Adminhtml_Yagostudio extends Mage_Adminhtml_Block_Widget_Grid_Container
{
    /**
     * constructor
     *
     * @access public
     * @return void
     * @author Ultimate Module Creator
     */
